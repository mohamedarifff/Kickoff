const mongoose = require("mongoose");

const supportUserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },

    password: {
      type: String,
      required: true,
    },

    role: {
      type: String,
      default: "support",
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("SupportUser", supportUserSchema);
